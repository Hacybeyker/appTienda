    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        // TODO add your handling code here:
        if(verificarCamposLlenos()){            
            cliente.setNombre(textoNombre.getText().trim().toUpperCase());
            cliente.setApellido(textoApellido.getText().trim().toUpperCase());
            cliente.setDni(textoDni.getText().trim().toUpperCase());
            cliente.setDireccion(textoDireccion.getText().trim().toUpperCase());
            cliente.setTelefono(textoTelefono.getText().trim().toUpperCase());
            cliente.setCelular(textoCelular.getText().trim().toUpperCase());
            cliente.setCorreo(textoCorreo.getText().trim().toUpperCase());
            cliente.setActivo(checkActivo.isSelected());
            GestionarClienteServicio gestionarClienteServicio  = new GestionarClienteServicio();
            int estado;
            if(tipo_accion==ACCION_CREAR){
                try{
                    estado = gestionarClienteServicio.crear(cliente);
                    if(estado > 0){
                        Mensaje.Mostrar_MENSAJE_GUARDADOEXITOSO(this);
                        this.dispose();
                    }
                }catch(Exception ex){
                    //JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                }            
            }else{
                try{
                    estado = gestionarClienteServicio.modificar(cliente);
                    if(estado > 0){
                        Mensaje.Mostrar_MENSAJE_MODIFICADOEXITOSO(this);
                        this.dispose();
                    }
                }catch(Exception ex){
                    //JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                }
            }
            
        }else{
            Mensaje.Mostrar_MENSAJE_LLENARCAMPOSOBLIGATORIOS(this);
            mostrarCampoVacio();
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

    private void botonSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSalirActionPerformed
        // TODO add your handling code here:
        if(verificarCamposLlenos()){            
            if(Mensaje.Mostrar_MENSAJE_SALIRSINGUARDAR(this))
                this.dispose();               
        }else
            this.dispose();
    }//GEN-LAST:event_botonSalirActionPerformed

    
    private boolean verificarCamposLlenos() {
        boolean estanLlenos;
        if(!(textoNombre.getText().trim().isEmpty() && textoApellido.getText().trim().isEmpty() && textoDni.getText().trim().isEmpty()))
            estanLlenos = true;
        else
            estanLlenos = false; 
        return estanLlenos;
    }

    private void mostrarCampoVacio(){
        if(textoNombre.getText().trim().isEmpty())
            textoNombre.requestFocus();
        else if (textoApellido.getText().isEmpty())
            textoApellido.requestFocus();
        else if (textoDni.getText().isEmpty())
            textoDni.requestFocus();        
    }
    
    
