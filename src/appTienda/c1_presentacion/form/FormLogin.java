    private boolean verificarCampos(){
        boolean estado;
        if(!textoUser.getText().trim().isEmpty()){
            if(comboCargo.getSelectedIndex()!=-1){
                if(!textoPassword.getText().trim().isEmpty()){
                   estado = true;
                }else{
                    textoPassword.requestFocus();
                    estado = false;
                }
            }else{
                comboCargo.requestFocus(); 
                comboCargo.setPopupVisible(true);
                estado = false;
            }
        }else{
            textoUser.requestFocus();
            estado = false;
        }
        return estado;
    }
    
