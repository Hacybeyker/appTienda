    private void ingresarEnter(java.awt.event.KeyEvent evt) throws HeadlessException {
        // TODO add your handling code here:   
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            if(verificarCampos()){
                if(verificarUsuario()){
                    FormMenu formMenu = new FormMenu(usuario);
                    formMenu.setVisible(true);
                    this.dispose();
                }else{
                    contador++;
                    Mensaje.Mostrar_MENSAJE_AVISOINTENTOS(this, contador);
                    if(contador>=3)
                        System.exit(0);
                }                
            }
        }
        
    }
    
    public boolean verificarUsuario(){
        boolean usuarioCorrecto;
        GestionarUsuarioServicio gestionarUsuarioServicio = new GestionarUsuarioServicio();
        String user,tipo,password;
        user = textoUser.getText().trim().toUpperCase();
        tipo = comboCargo.getSelectedItem().toString();
        password = textoPassword.getText().trim();
        try {
            usuario = gestionarUsuarioServicio.IngresoUsuario(user, tipo, password);
            if(usuario!=null){
                usuarioCorrecto = true;
            }else{
                usuarioCorrecto = false;
            }
        } catch (Exception ex) {
            usuarioCorrecto = false;
        }
        return usuarioCorrecto;
    }

