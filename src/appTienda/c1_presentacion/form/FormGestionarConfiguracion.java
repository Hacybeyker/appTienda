    private void checkModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkModificarActionPerformed
        // TODO add your handling code here:
        if(checkModificar.isSelected()){
            textoNuevoValor.setEnabled(true);
            textoNuevoValor.setEditable(true);
            textoNuevoValor.selectAll();
            textoNuevoValor.requestFocus();
        }else{
            textoNuevoValor.setEnabled(false);
            textoNuevoValor.setEditable(false);
        }
    }//GEN-LAST:event_checkModificarActionPerformed

    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        // TODO add your handling code here:
        long empezarDesdeEsteCodigo = Long.parseLong(textoNuevoValor.getText());
        RegistrarPedidoServicio registrarPedidoServicio = new RegistrarPedidoServicio();
        try {
            registrarPedidoServicio.IngresarNuevoCodigo(empezarDesdeEsteCodigo);
            Mensaje.Mostrar_MENSAJE_GUARDADOEXITOSO(this);
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(FormGestionarConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

